package day_7_conditional;

public class Chars {

	public static void main(String[] args) {
		char letter = 'a';
		letter++;
		System.out.println(letter);
		
		int ascii = letter;
		System.out.println(ascii);
		
		char myChar = 'B';
		System.out.println(myChar);

		myChar--;
		System.out.println(myChar);
		
		char symbol = '@';
		symbol--;
		System.out.println(symbol);
		
		char ch = 100;
		System.out.println(ch);
	}

}
