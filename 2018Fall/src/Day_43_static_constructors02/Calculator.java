package Day_43_static_constructors02;

public class Calculator {

	
	
	
	public static double plus(int num1, int num2) {
		return num1 + num2;
	}

	public static double minus(int num1, int num2) {
		return num1 - num2;
	}

	public static double multiply(int num1, int num2) {
		return num1 * num2;
	}
}
